CREATE TABLE EMP_DDL(
    EMPNO NUMBER(4),
    ENAME VARCHAR2(10),
    JOB VARCHAR2(9),
    MGR NUMBER(4),
    HIREDATE DATE,
    SAL NUMBER(7,2), --전체 자리수 : 7자리 / 소수점 아래 2자리 포함 가능 = 자연수 자리 5자리
