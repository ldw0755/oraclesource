--COLUMN 추가
ALTER TABLE EMP_DDL ADD HP VARCHAR2(20);

--기존 COLUMN명 변경
ALTER TABLE EMP_DDL RENAME COLUMN HP TO TEL; --생긴게 영어문법

DESC EMP_DDL;

--기존 TYPE 변경
ALTER TABLE EMP_DDL MODIFY EMPNO NUMBER(5);

--기존 컬럼 삭제
ALTER TABLE EMP_DDL DROP COLUMN TEL;

--테이블 이름 변경
RENAME EMP_DDL TO EMP_RENAME;

DESC EMP_RENAME;

